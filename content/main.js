/**
 * Content script entry point.
 * Orchestrates subtitle translation and page element translation on YouTube pages.
 */
(function () {
  var state = {
    initialized: false,
    videoId: null,
    subtitleInitDone: false,
    subtitleGeneration: 0,   // 代际计数器，防止重复提取
    settings: {
      subtitle: true,
      comments: true,
      title: true,
      description: true,
      liveChat: true
    }
  };

  var subTranslator = new YTTranslate.Translator();

  /**
   * Load settings from chrome.storage and initialize.
   */
  function loadSettings() {
    return new Promise(function (resolve) {
      chrome.storage.local.get(['settings', 'apiKey'], function (result) {
        if (result.settings) {
          for (var key in result.settings) {
            state.settings[key] = result.settings[key];
          }
        }
        resolve();
      });
    });
  }

  /**
   * Detect if current page is a YouTube video page.
   */
  function isWatchPage() {
    return /^\/watch/.test(location.pathname) || !!getVideoId();
  }

  /**
   * Extract video ID from URL.
   */
  function getVideoId() {
    var params = new URLSearchParams(location.search);
    return params.get('v') || null;
  }

  /**
   * Initialize subtitle translation system.
   *
   * 流程：
   *   1. 调用 extractSubtitles() → 触发 CC 按钮 + 等待 MAIN world 拦截 timedtext
   *   2. 解析出字幕数组 → 批量翻译
   *   3. 初始化覆盖层显示双语字幕
   */
  function initSubtitle() {
    if (!state.settings.subtitle) return;

    var videoId = getVideoId();
    if (!videoId) return;

    // 防止同一视频重复初始化
    if (state.videoId === videoId && state.subtitleInitDone) return;
    state.videoId = videoId;
    state.subtitleInitDone = true;

    // 代际计数器：丢弃旧提取的结果
    state.subtitleGeneration++;
    var myGen = state.subtitleGeneration;

    console.log('YT Translate: Starting subtitle extraction for ' + videoId + ' (gen ' + myGen + ')');

    YTTranslate.extractor.extractSubtitles().then(function (result) {
      // 如果不是最新一次提取，丢弃结果
      if (myGen !== state.subtitleGeneration) {
        console.log('YT Translate: Discarding stale extraction result (gen ' + myGen +
          ', current gen ' + state.subtitleGeneration + ')');
        return;
      }

      if (!result || !result.subtitles || result.subtitles.length === 0) {
        console.log('YT Translate: No subtitles available for this video');
        YTTranslate.overlay.init([], videoId);
        YTTranslate.overlay.showNoCaptions();
        return;
      }

      console.log(
        'YT Translate: Found ' + result.subtitles.length + ' subtitles [' +
        (result.trackInfo ? result.trackInfo.languageCode : '?') + ']' +
        (result.trackInfo && result.trackInfo.isAutoGenerated ? ' (auto)' : '')
      );

      // 初始化覆盖层（先显示原文）
      YTTranslate.overlay.init(result.subtitles, videoId);

      // === 诊断：直接测试翻译管道是否正常 ===
      subTranslator.translate('こんにちは').then(function (testResult) {
        console.log('YT Translate: TRANSLATION TEST OK — "こんにちは" → "' + testResult + '"');
      }).catch(function (testErr) {
        console.error('YT Translate: TRANSLATION TEST FAILED — ' + testErr.message);
      });

      // 已成功捕获字幕 → 关闭 YouTube 原生字幕（避免双层显示）
      var ccBtn = document.querySelector('.ytp-subtitles-button');
      if (ccBtn && ccBtn.getAttribute('aria-pressed') === 'true') {
        ccBtn.click();
        console.log('YT Translate: Disabled YouTube native captions to avoid double display');
      }

      var subtitles = result.subtitles;
      var texts = [];
      for (var i = 0; i < subtitles.length; i++) {
        texts.push(subtitles[i].text);
      }

      // 后台批量翻译——慢慢填充缓存，不阻塞实时翻译
      // 实时翻译由 overlay.js 的 _requestTranslation 负责（字幕出现时立即翻译当前条）
      var BATCH_SIZE = 30;
      var batchIdx = 0;
      var totalBatches = Math.ceil(texts.length / BATCH_SIZE);

      function backgroundBatch() {
        if (myGen !== state.subtitleGeneration) return;
        if (batchIdx >= totalBatches) {
          console.log('YT Translate: Background translation cache filled');
          return;
        }
        var batchTexts = texts.slice(batchIdx * BATCH_SIZE, (batchIdx + 1) * BATCH_SIZE);
        subTranslator.translateBatch(batchTexts).then(function () {
          if (myGen !== state.subtitleGeneration) return;
          batchIdx++;
          // 每 20 批报告一次进度
          if (batchIdx % 20 === 0) {
            console.log('YT Translate: Cache fill: ' + batchIdx + '/' + totalBatches + ' batches');
          }
          // 间隔 2 秒再发下一批，避免 API 限流
          setTimeout(backgroundBatch, 2000);
        }).catch(function (err) {
          console.warn('YT Translate: Cache batch failed:', err.message);
          if (myGen !== state.subtitleGeneration) return;
          batchIdx++;
          setTimeout(backgroundBatch, 2000);
        });
      }
      // 延迟 5 秒后开始填充（优先让实时翻译拿到第一批）
      setTimeout(backgroundBatch, 5000);

    }).catch(function (err) {
      console.warn('YT Translate: Subtitle extraction failed —', err.message);
    });
  }

  /**
   * Initialize page element translation.
   */
  function initPageTranslation() {
    var pageSettings = {
      title: state.settings.title,
      description: state.settings.description,
      comments: state.settings.comments,
      liveChat: state.settings.liveChat
    };
    YTTranslate.observer.start(pageSettings);
    console.log('YT Translate: Page translation observer started');
  }

  /**
   * Full initialization: load settings, then init all systems.
   */
  function initialize() {
    if (state.initialized) return;

    // iframe 防护：只在聊天 iframe 内做页面翻译，跳过字幕等重逻辑
    var isIframe = (window.top !== window);
    var isChatFrame = isIframe && (
      location.href.indexOf('live_chat') !== -1 ||
      location.href.indexOf('chatframe') !== -1
    );

    if (isIframe && !isChatFrame) {
      // 无关 iframe（广告等）→ 跳过
      return;
    }

    loadSettings().then(function () {
      // 聊天 iframe：只做页面翻译（聊天消息），跳过字幕和 watch page 检查
      if (isChatFrame) {
        state.initialized = true;
        if (document.readyState === 'loading') {
          document.addEventListener('DOMContentLoaded', initPageTranslation);
        } else {
          initPageTranslation();
        }
        return;
      }

      if (!isWatchPage()) {
        return;
      }

      state.initialized = true;

      var retries = 0;
      function tryInitSubtitle() {
        var video = document.querySelector('video');
        if (video && video.readyState >= 1) {
          initSubtitle();
        } else if (retries < 20) {
          retries++;
          setTimeout(tryInitSubtitle, 500);
        } else {
          initSubtitle();
        }
      }
      setTimeout(tryInitSubtitle, 1000);

      if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initPageTranslation);
      } else {
        initPageTranslation();
      }
    });
  }

  /**
   * Teardown current state for SPA navigation.
   */
  function teardown() {
    YTTranslate.overlay.destroy();
    YTTranslate.observer.stop();
    state.initialized = false;
    state.videoId = null;
    state.subtitleInitDone = false;

    // 清除 MAIN world 拦截器中可能残留的旧视频数据
    window.postMessage({
      target: 'yt-translate-fetcher',
      type: 'YT_CLEAR_INTERCEPTED'
    }, '*');
  }

  // --- Bootstrap ---

  setTimeout(initialize, 500);

  document.addEventListener('yt-navigate-finish', function () {
    teardown();
    setTimeout(initialize, 500);
  });

  chrome.runtime.onMessage.addListener(function (request, sender, sendResponse) {
    if (request.type === 'SETTINGS_UPDATED') {
      teardown();
      state.settings = request.settings;
      setTimeout(initialize, 300);
      sendResponse({ success: true });
    }
    if (request.type === 'GET_STATUS') {
      sendResponse({
        initialized: state.initialized,
        videoId: state.videoId,
        settings: state.settings
      });
    }
    if (request.type === 'REINIT') {
      teardown();
      setTimeout(initialize, 300);
      sendResponse({ success: true });
    }
    if (request.type === 'CLEAR_CACHE') {
      subTranslator.clearCache();
      sendResponse({ success: true });
    }
  });

})();
